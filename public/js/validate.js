function isMSSVValid(mssv) {
    let validMSSV = /^[0-9]+$/;
    return validMSSV.test(mssv);
}

function isEmailValid(email) {
    let validEmailFormat = /^[\w-\.]+@([\w-]+\.)+[\w-]{2,4}$/;
    return validEmailFormat.test(email);
}

